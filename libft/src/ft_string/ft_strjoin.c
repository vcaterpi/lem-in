#include "libft.h"

char				*ft_strjoin(char const *s1, char const *s2)
{
	char			*str;
	size_t			n;

	if (s1 == NULL && s2 == NULL)
		return (NULL);
	else if (s1 == NULL)
		return (ft_strdup(s2));
	else if (s2 == NULL)
		return (ft_strdup(s1));
	n = ft_strlen(s1) + ft_strlen(s2);
	str = ft_strnew(n);
	if (str == NULL)
		return (NULL);
	while (*s1)
		*str++ = *s1++;
	while (*s2)
		*str++ = *s2++;
	*str = 0;
	return (str - n);
}
